package com.Coffee.Delivery.entity;

public class Consumer {

    private int id;
    private String name;

    public Consumer() {
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
